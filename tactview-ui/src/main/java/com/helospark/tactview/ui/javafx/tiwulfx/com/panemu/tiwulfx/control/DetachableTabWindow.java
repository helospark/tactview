package com.helospark.tactview.ui.javafx.tiwulfx.com.panemu.tiwulfx.control;

public class DetachableTabWindow {
    LeafElement pane;
    double x, y;
    double width, height;
    public LeafElement getPane() {
        return pane;
    }

    public void setPane(LeafElement pane) {
        this.pane = pane;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

}
